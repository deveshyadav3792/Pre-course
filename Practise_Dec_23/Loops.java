package Practise_Dec_23;

public class Loops {

	public static void main(String[] args) {
		int num;
		//print numbers from 1 to 5
		for (num = 1; num <= 10; num = num+1) {
			System.out.println(num);
		}
		
		System.out.println(num);//6
		System.out.println("hello");
	}

}

